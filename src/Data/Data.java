//memiliki fungsi untuk menampilkan select database ke table
private void Tampil(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM sepatu");
            
            DefaultTableModel model= new DefaultTableModel();
            model.addColumn("KodeSepatu");
            model.addColumn("Jenis");
            model.addColumn("Merek");
            model.addColumn("Berat");
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()){
                Object[] data = {
                    rs.getString("KodeSepatu"),
                    rs.getString("Jenis"),
                    rs.getString("Merek"),
                    rs.getString("Berat")
                    
            };
                model.addRow(data);
                TabelSepatu.setModel(model);
            }
        }catch (Exception e){
        }
    }
