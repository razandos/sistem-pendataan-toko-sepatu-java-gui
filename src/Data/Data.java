    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        // TODO add your handling code here:

        try{
            //MEMILIKI FUNGSI UNTUK MENGECEK APAKAH ADA DATA YANG SAMA
            if (BSimpan.getText()=="Save"){
                String cek = "SELECT * FROM sepatu WHERE KodeSepatu = '"+ TextKode.getText()+"'";
                rs = st.executeQuery(cek);
                if(rs.next()){
                    JOptionPane.showMessageDialog(null,"Data sudah ada");
                    //KALAU TIDAK ADA DIA AKAN MELAKNJUTKAN MEMASUKKAN DATA KE DATABASE
                }else{
                    String sql = "INSERT INTO sepatu VALUES ('"+TextKode.getText()+
                    "','" + TextJenis.getText()+
                    "','" + TextMerek.getText()+
                    "','"+ TextBerat.getText()+"')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"Data sudah tersimpan");
                    Tampil();
                    hapus();
                }
                //MEMILIKI FUNGSI UNTUK UPDATE DATA
            }else{
                String update = "UPDATE sepatu SET Jenis = '" + TextJenis.getText()+
                "', Merek = '" +TextMerek.getText()+
                "', Berat = '" + TextBerat.getText()+
                "' WHERE KodeSepatu ='" + TextKode.getText()+"'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null,"Data sudah tersimpan");
                Tampil();
                hapus();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_BSimpanActionPerformed

